; Exercise 1: signed and unsigned operations
;
; Visual Studio EFLAGS
;
;	| Overflow  | OV |
;	| Direction | UP |
;	| Interrupt | EI |
;	| Sign      | PL |
;	| Zero      | ZR |
;	| Auxiliary | AC |
;	| Parity    | PE |
;	| Carry     | CY |
;
; AH | AL - 1 byte
; AX      - 2 bytes
; EAX     - 4 bytes
;
;  AX Range :
;     0       => 65,535  (unsigned)
;     -32,768 => 32,767  (signed) 
;

.386
.model flat,stdcall
.stack 4096
ExitProcess proto,dwExitCode:dword

.code
main proc

	mov eax, 0    ; debug purpose only

	mov ax, 78f0h
	;=========================================
	;  78F0
	;    7 = 0111
	;    8 = 1000
	;    F = 1111
	;    0 = 0000
	;              0111 1000 1111 0000 (b)
	;                            30960 (d)
	;=========================================

	add ax,10h
	;==========================================================================
	;  10h
	;      0000 0000 0001 0000
	;
	;       78F0h
	;     +   10h
	;      ======
	;       7900h
	;                         0111 1000 1111 0000
	;                       + 0000 0000 0001 0000
	;                      -----------------------
	;                         0111 1001 0000 0000 (30976d)
	;                              ah       al
	;     EFLAGS
	;            SF = 0, ZF = 0, CF = 0 , OF = 0
	;
	;     VS output : OV = 0 UP = 0 EI = 1 PL = 0 ZR = 0 AC = 0 PE = 1 CY = 0 
	;==========================================================================

	add ah,1
	;==========================================================================
	;   ah : 0111 1001  
	;   1  : 0000 0001
	; 
	;   add => 
	;             0111 1001
	;           + 0000 0001
	;            ===========
	;             0111 1010    (7Ah) (122d)
	;
	;     EFLAGS
	;            SF = 0, ZF = 0, CF = 0 , OF = 0
	;
	;     VS output : OV = 0 UP = 0 EI = 1 PL = 0 ZR = 0 AC = 0 PE = 0 CY = 0 
	;==========================================================================

	add ax,2
	;==========================================================================
	;   ax : 7A00  ( 0111 1010 0000 0000h) ( 31232d)
	;   2  : 0000 0000 0000 0010
	; 
	;   add => 
	;             0111 1010 0000 0000
	;           + 0000 0000 0000 0010
	;            =====================
	;             0111 0010 0000 0010   (7A02h) (31234d)
	;
	;     EFLAGS
	;            SF = 0, ZF = 0, CF = 0 , OF = 0
	;
	;     VS output : OV = 0 UP = 0 EI = 1 PL = 0 ZR = 0 AC = 0 PE = 0 CY = 0 
	;==========================================================================


	invoke ExitProcess,0
main endp
end main